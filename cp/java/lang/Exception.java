package java.lang;

public class Exception extends Throwable {

  public Exception() {
    super();
  }

  public Exception(final java.lang.String message) {
    super(message);
  }

  public Exception(final java.lang.String message, final java.lang.Throwable cause) {
    super(message, cause);
  }

  public Exception(final java.lang.Throwable cause) {
    super(cause);
  }

}
