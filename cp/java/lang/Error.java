package java.lang;

public class Error extends Throwable {

  public Error() {
    super();
  }

  public Error(final java.lang.String message) {
    super(message);
  }

  public Error(final java.lang.String message, final java.lang.Throwable cause) {
    super(message, cause);
  }

  public Error(final java.lang.Throwable cause) {
    super(cause);
  }

}
