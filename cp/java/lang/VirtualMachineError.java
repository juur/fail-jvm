package java.lang;

public class VirtualMachineError extends Error {

  public VirtualMachineError() {
    super();
  }

  public VirtualMachineError(final java.lang.String message) {
    super(message);
  }

  public VirtualMachineError(final java.lang.String message, final java.lang.Throwable cause) {
    super(message, cause);
  }

  public VirtualMachineError(final java.lang.Throwable cause) {
    super(cause);
  }

}
